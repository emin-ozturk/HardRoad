package com.emin.hardroad;

public class Rhombus {
    private float x, y, w;
    public Rhombus(float x, float y, float w) {
        this.x = x;
        this.y = y;
        this.w = w;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getW() {
        return w;
    }
}
